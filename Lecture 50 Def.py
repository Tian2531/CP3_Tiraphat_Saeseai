#Def มีไว้สำหรับการประกาศฟังค์ชั่น
#x,y คือ parameter (ช่องสำหรับใส่ข้อมูลลงไปตามรูปแบบที่เรากำหนด)
def Addition_number(x,y):
    print(x+y)
def Subtraction_number(x,y):
    print(x-y)
def Multiplication_number(x,y):
    print(x*y)
def Division_number(x,y):
    print(x/y)

Addition_number(100,10)
Subtraction_number(100,10)
Multiplication_number(100,10)
Division_number(100,10)
